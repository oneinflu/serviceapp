file_path = '/Users/suuryaprabhat/Desktop/serviceapp/lib/l10n/app_localizations.dart'
with open(file_path, 'r', encoding='utf-8') as f:
    content = f.read()

for lang in ['en', 'hi', 'kn', 'ta', 'te', 'ml', 'mr', 'gu', 'or']:
    pattern = r"('" + lang + r"': \{)(.*?)(    \},)"
    match = re.search(pattern, content, flags=re.DOTALL)
    if match:
        dict_content = match.group(2)
        new_items = ""
        for k, v in new_keys.items():
            val = v[lang].replace("'", "\\'").replace('\n', '\\n')
            new_items += f"      '{k}': '{val}',\n"
        
        replacement = match.group(1) + dict_content + new_items + match.group(3)
        content = content.replace(match.group(0), replacement)

with open(file_path, 'w', encoding='utf-8') as f:
    f.write(content)

